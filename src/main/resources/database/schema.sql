    M1 TIMESTAMP ,
    M2 TIMESTAMP,
    M3 TIMESTAMP,
    M4 TIMESTAMP,
    M5 TIMESTAMP,
    M6 TIMESTAMP,
    M10 TIMESTAMP,
    M15 TIMESTAMP,
    M30 TIMESTAMP,
    H1 TIMESTAMP,
    H4 TIMESTAMP,
    D1 TIMESTAMP,
    W1 TIMESTAMP,
    MN TIMESTAMP,
    INDEX EXCHANGE_TRANSACTION_TOOL_NAME_INDEX (TOOL_NAME),
    INDEX EXCHANGE_TRANSACTION_M1_INDEX (M1),
    INDEX EXCHANGE_TRANSACTION_M2_INDEX (M2),
    INDEX EXCHANGE_TRANSACTION_M3_INDEX (M3),
    INDEX EXCHANGE_TRANSACTION_M4_INDEX (M4),
    INDEX EXCHANGE_TRANSACTION_M5_INDEX (M5),
    INDEX EXCHANGE_TRANSACTION_M6_INDEX (M6),
    INDEX EXCHANGE_TRANSACTION_M10_INDEX (M10),
    INDEX EXCHANGE_TRANSACTION_M15_INDEX (M15),
    INDEX EXCHANGE_TRANSACTION_M30_INDEX (M30),
    INDEX EXCHANGE_TRANSACTION_H1_INDEX (H1),
    INDEX EXCHANGE_TRANSACTION_H4_INDEX (H4),
    INDEX EXCHANGE_TRANSACTION_D1_INDEX (D1),
    INDEX EXCHANGE_TRANSACTION_W1_INDEX (W1),
    INDEX EXCHANGE_TRANSACTION_MN_INDEX (MN)
);
