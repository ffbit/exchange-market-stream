CREATE TABLE IF NOT EXISTS CONTRACT (
    ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(10) NOT NULL,
    VOLUME BIGINT NOT NULL,
    DEAL_TIMESTAMP TIMESTAMP NOT NULL,
    M1 TIMESTAMP ,
    M2 TIMESTAMP,
    M3 TIMESTAMP,
    M4 TIMESTAMP,
    M5 TIMESTAMP,
    M6 TIMESTAMP,
    M10 TIMESTAMP,
    M15 TIMESTAMP,
    M30 TIMESTAMP,
    H1 TIMESTAMP,
    H4 TIMESTAMP,
    D1 TIMESTAMP,
    W1 TIMESTAMP,
    MN TIMESTAMP,
    INDEX CONTRACT_NAME_INDEX (NAME),
    INDEX CONTRACT_M1_INDEX (M1),
    INDEX CONTRACT_M2_INDEX (M2),
    INDEX CONTRACT_M3_INDEX (M3),
    INDEX CONTRACT_M4_INDEX (M4),
    INDEX CONTRACT_M5_INDEX (M5),
    INDEX CONTRACT_M6_INDEX (M6),
    INDEX CONTRACT_M10_INDEX (M10),
    INDEX CONTRACT_M15_INDEX (M15),
    INDEX CONTRACT_M30_INDEX (M30),
    INDEX CONTRACT_H1_INDEX (H1),
    INDEX CONTRACT_H4_INDEX (H4),
    INDEX CONTRACT_D1_INDEX (D1),
    INDEX CONTRACT_W1_INDEX (W1),
    INDEX CONTRACT_MN_INDEX (MN)
);
